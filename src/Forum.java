public class Forum {
    private String idForum;
    private String topic;
    private int numberOfComments;


    //Getters
    public String getIdForum(){return this.idForum;}
    public String getTopic(){return this.topic;}
    public int getNumberOfComments(){return this.numberOfComments;}

    //Setters
    public void setIdForum(String idForum){this.idForum = idForum;}
    public void setTopic(String topic){this.topic = topic;}
    public void setNumberOfComments(int numberOfComments){this.numberOfComments = numberOfComments;}
    
}
